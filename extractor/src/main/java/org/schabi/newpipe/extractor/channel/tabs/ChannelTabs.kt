    @JvmField
    val VIDEOS: String = "videos"
    @JvmField
    val TRACKS: String = "tracks"
    @JvmField
    val SHORTS: String = "shorts"
    @JvmField
    val LIVESTREAMS: String = "livestreams"
    @JvmField
    val CHANNELS: String = "channels"
    @JvmField
    val PLAYLISTS: String = "playlists"
    @JvmField
    val ALBUMS: String = "albums"
}
